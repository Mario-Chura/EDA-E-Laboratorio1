public class javaBooleans {
  public static void main(String[] args) {
    
    	/*Operacion Booleana*/
    	//Es la expresion que devuelve un valor TRUE o FALSE
	boolean isJavaFun = true;
	boolean isFishTasty = false;
	System.out.println(isJavaFun);     // Devuelve true
	System.out.println(isFishTasty);   // Devuelve false
        //Se combina con operadores de combinacion para determinar la verdad o falsedad de las sentencias
        int x = 10;
	int y = 9;
	System.out.println(x > y); // retorna true, porque 10 es mayor que 9
	System.out.println(x == 10); // retorna true, porque el valor de x es igual a 10
  }
}
