public class Javastring {
    public static void main(String[] args) {
        String nombre = "Cielo";
        System.out.println("mi nombre es " + nombre);
        System.out.println("el largo de mi nombre es " + nombre.length());
        System.out.println("mi nombre en mayuscula es " + nombre.toUpperCase());
        System.out.println("mi nombre en mayuscula es " + nombre.toLowerCase());
        System.out.println("la letra e de mi nombre esta en el indice " + nombre.indexOf("e"));
    }
}
